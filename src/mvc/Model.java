package mvc;

public class Model {

	// the Model class, in these example, is a Student class
	private String id;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String ID) {
		this.id = ID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}